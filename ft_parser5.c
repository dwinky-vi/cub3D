#include "head_cub3d.h"

int		ft_check_file_cub(char *str)
{
	int k;

	k = 0;
	while (str[k])
	{
		if (str[k] == '.' && str[k + 1] == 'c' && str[k + 2] == 'u' &&
			str[k + 3] == 'b' && str[k + 4] == '\0')
			return (open(str, O_RDONLY));
		k++;
	}
	return (-1);
}
