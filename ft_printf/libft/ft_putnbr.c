/*
** -9223372036854775808
*/

#include "libft.h"

void	ft_putnbr(int n)
{
	char num;

	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n *= -1;
	}
	if (0 <= n && n <= 9)
	{
		num = n + (int)'0';
		write(1, &num, 1);
		return ;
	}
	ft_putnbr(n / 10);
	ft_putnbr(n % 10);
}
